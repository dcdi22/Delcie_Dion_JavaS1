package com.company;

public class Calculator {

//    ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
//    ADD

    public static int add(int a, int b) {
        return a+b;
    }

//    ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

//    SUBTRACT
    public static int subtract(int a, int b) {
        return a - b;
    }

//    ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

//    MULTIPLY
    public static int multiply(int a, int b) {return a * b;}

//    ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
//    DIVIDE
    public static int divide(int a, int b) {return a / b;}


}
